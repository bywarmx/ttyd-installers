#!/usr/bin/env bash
set -euo pipefail

DOMAIN="${DOMAIN:-lobosolitario.tk}"
DOCROOT="${DOCROOT:-/var/www/html}"

TTYD_VERSION="${TTYD_VERSION:-1.7.7}"
TTYD_URL="${TTYD_URL:-https://github.com/tsl0922/ttyd/releases/download/${TTYD_VERSION}/ttyd.x86_64}"
TTYD_BIN="${TTYD_BIN:-/usr/local/bin/ttyd-static}"
TTYD_USER="${TTYD_USER:-bywarrior}"
TTYD_PASSWORD="${TTYD_PASSWORD:-010286}"
TTYD_RUN_USER="${TTYD_RUN_USER:-bywarrior}"
TTYD_RUN_GROUP="${TTYD_RUN_GROUP:-${TTYD_RUN_USER}}"
TTYD_HOME="${TTYD_HOME:-/home/${TTYD_RUN_USER}}"
TTYD_CWD="${TTYD_CWD:-${TTYD_HOME}}"
TTYD_SHELL="${TTYD_SHELL:-/bin/bash}"
TTYD_PROTECTED_PORT="${TTYD_PROTECTED_PORT:-7681}"
TTYD_FM_PORT="${TTYD_FM_PORT:-7682}"
TTYD_PROTECTED_PATH="${TTYD_PROTECTED_PATH:-/ttyd}"
TTYD_FM_FONT_SIZE="${TTYD_FM_FONT_SIZE:-11}"
TTYD_TERM="${TTYD_TERM:-xterm-256color}"
TTYD_COLORTERM="${TTYD_COLORTERM:-truecolor}"
TERMINAL_BYPASS_TOKEN="${TERMINAL_BYPASS_TOKEN:-53109730356104db67d2e0bafae31dd88c169eb92dc2247c}"

if [[ $EUID -ne 0 ]]; then
  echo "Ejecuta con sudo: sudo $0"
  exit 1
fi

need_cmd() {
  command -v "$1" >/dev/null 2>&1 || {
    echo "Falta comando requerido: $1"
    exit 1
  }
}

need_cmd curl
need_cmd apache2ctl
need_cmd systemctl
need_cmd base64
need_cmd sed
need_cmd id
need_cmd getent

enable_user_terminal_colors() {
  local bashrc="${TTYD_HOME}/.bashrc"
  [[ -f "$bashrc" ]] || return 0

  cp -n "$bashrc" "${bashrc}.bak-ttyd-colors" 2>/dev/null || true

  if grep -q '^#force_color_prompt=yes' "$bashrc"; then
    sed -i 's/^#force_color_prompt=yes/force_color_prompt=yes/' "$bashrc"
  elif ! grep -q '^force_color_prompt=yes' "$bashrc"; then
    printf '\nforce_color_prompt=yes\n' >>"$bashrc"
  fi

  if grep -q '^#export GCC_COLORS=' "$bashrc"; then
    sed -i 's/^#export GCC_COLORS=/export GCC_COLORS=/' "$bashrc"
  elif ! grep -q '^export GCC_COLORS=' "$bashrc"; then
    printf "export GCC_COLORS='error=01;31:warning=01;35:note=01;36:caret=01;32:locus=01:quote=01'\n" >>"$bashrc"
  fi
}

if ! id "$TTYD_RUN_USER" >/dev/null 2>&1; then
  echo "No existe el usuario real para ttyd: ${TTYD_RUN_USER}"
  exit 1
fi

if ! getent group "$TTYD_RUN_GROUP" >/dev/null 2>&1; then
  echo "No existe el grupo real para ttyd: ${TTYD_RUN_GROUP}"
  exit 1
fi

mkdir -p "$DOCROOT"
mkdir -p "$TTYD_CWD"
chown "${TTYD_RUN_USER}:${TTYD_RUN_GROUP}" "$TTYD_CWD"
sed -n "/^: <<'__FILEMANAGER_PHP_B64_DATA__'$/,/^__FILEMANAGER_PHP_B64_DATA__$/p" "$0" \
  | sed '1d;$d' \
  | base64 -d > "$DOCROOT/filemanager.php"
sed -i "s/53109730356104db67d2e0bafae31dd88c169eb92dc2247c/${TERMINAL_BYPASS_TOKEN}/g" "$DOCROOT/filemanager.php"
chmod 0664 "$DOCROOT/filemanager.php"

tmp_ttyd="$(mktemp)"
curl -L -o "$tmp_ttyd" "$TTYD_URL"
install -m 0755 "$tmp_ttyd" "$TTYD_BIN"
rm -f "$tmp_ttyd"

a2enmod proxy proxy_http proxy_wstunnel rewrite >/dev/null
enable_user_terminal_colors

cat >/etc/apache2/conf-available/filemanager-ttyd.conf <<EOF
ProxyPreserveHost On
ProxyRequests Off
RewriteEngine On

RewriteCond %{HTTP:Cookie} (^|;\ *)FM_TTYD=${TERMINAL_BYPASS_TOKEN}(;|$)
RewriteCond %{HTTP:Upgrade} =websocket [NC]
RewriteRule ^${TTYD_PROTECTED_PATH}/(.*) ws://127.0.0.1:${TTYD_FM_PORT}${TTYD_PROTECTED_PATH}/\$1 [P,L]
RewriteCond %{HTTP:Cookie} (^|;\ *)FM_TTYD=${TERMINAL_BYPASS_TOKEN}(;|$)
RewriteRule ^${TTYD_PROTECTED_PATH}/(.*) http://127.0.0.1:${TTYD_FM_PORT}${TTYD_PROTECTED_PATH}/\$1 [P,L]

ProxyPass "${TTYD_PROTECTED_PATH}/ws" "ws://127.0.0.1:${TTYD_PROTECTED_PORT}${TTYD_PROTECTED_PATH}/ws"
ProxyPassReverse "${TTYD_PROTECTED_PATH}/ws" "ws://127.0.0.1:${TTYD_PROTECTED_PORT}${TTYD_PROTECTED_PATH}/ws"
ProxyPass "${TTYD_PROTECTED_PATH}/" "http://127.0.0.1:${TTYD_PROTECTED_PORT}${TTYD_PROTECTED_PATH}/"
ProxyPassReverse "${TTYD_PROTECTED_PATH}/" "http://127.0.0.1:${TTYD_PROTECTED_PORT}${TTYD_PROTECTED_PATH}/"
EOF
a2enconf filemanager-ttyd >/dev/null

cat >/etc/systemd/system/ttyd-protected.service <<EOF
[Unit]
Description=ttyd protected terminal for ${DOMAIN}
After=network.target

[Service]
Type=simple
User=${TTYD_RUN_USER}
Group=${TTYD_RUN_GROUP}
Environment=TERM=${TTYD_TERM}
Environment=COLORTERM=${TTYD_COLORTERM}
Environment=HOME=${TTYD_HOME}
Environment=SHELL=${TTYD_SHELL}
WorkingDirectory=${TTYD_CWD}
ExecStart=${TTYD_BIN} -i 127.0.0.1 -p ${TTYD_PROTECTED_PORT} -b ${TTYD_PROTECTED_PATH} -W -w ${TTYD_CWD} -c ${TTYD_USER}:${TTYD_PASSWORD} ${TTYD_SHELL}
Restart=always
RestartSec=2

[Install]
WantedBy=multi-user.target
EOF

cat >/etc/systemd/system/ttyd-filemanager.service <<EOF
[Unit]
Description=ttyd embedded terminal for filemanager on ${DOMAIN}
After=network.target

[Service]
Type=simple
User=${TTYD_RUN_USER}
Group=${TTYD_RUN_GROUP}
Environment=TERM=${TTYD_TERM}
Environment=COLORTERM=${TTYD_COLORTERM}
Environment=HOME=${TTYD_HOME}
Environment=SHELL=${TTYD_SHELL}
WorkingDirectory=${TTYD_CWD}
ExecStart=${TTYD_BIN} -i 127.0.0.1 -p ${TTYD_FM_PORT} -b ${TTYD_PROTECTED_PATH} -W -w ${TTYD_CWD} -t fontSize=${TTYD_FM_FONT_SIZE} -t fontFamily=ui-monospace,SFMono-Regular,Consolas,monospace ${TTYD_SHELL}
Restart=always
RestartSec=2

[Install]
WantedBy=multi-user.target
EOF

systemctl daemon-reload
systemctl enable --now ttyd-protected.service ttyd-filemanager.service

apache2ctl configtest
systemctl reload apache2

echo "Instalacion completa."
echo "Filemanager: https://${DOMAIN}/filemanager.php?p="
echo "Terminal directa protegida: https://${DOMAIN}${TTYD_PROTECTED_PATH}/"
echo "Usuario terminal directa: ${TTYD_USER}"
echo "Password terminal directa: ${TTYD_PASSWORD}"
echo "Shell ejecutado como: ${TTYD_RUN_USER}:${TTYD_RUN_GROUP}"
echo "Terminal embebida para filemanager: https://${DOMAIN}${TTYD_PROTECTED_PATH}/ con cookie FM_TTYD"
exit 0

